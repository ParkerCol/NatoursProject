const mongoose = require('mongoose');

//Creating schema for a collection insidee the DataBes (shema is the structure of the collection)
const tourSchema = new mongoose.Schema({
  //Schema type options
  name: {
    type: String,
    required: [true, 'A tour must have a name'],
    unique: true,
  },
  duration: {
    type: Number,
    required: [true, 'A tour must have a duration'],
  },
  maxGroupSize: {
    type: Number,
    required: [true, 'A tour must have a group size'],
  },
  difficulty: {
    type: String,
    required: [true, 'A tour must have a difficulty'],
  },
  ratingsAvarage: {
    type: Number,
    default: 4.5,
  },
  ratingsQuantity: {
    type: Number,
    default: 0,
  },
  price: {
    type: Number,
    required: [true, 'A tour must have a price'],
  },
  priceDiscount: Number,
  summary: {
    type: String,
    trim: true,
    required: [true, 'A tour must have a description'],
  },
  description: {
    type: String,
    trim: true,
  },
  imageCover: {
    type: String,
    required: [true, 'A tour must have a cover image'],
  },
  images: [String],
  createdAt: {
    type: Date,
    //default: Date.now() is in milliseconds and mongoose will convert
    default: Date.now(),
  },
  startDates: [Date],
});

//Creating a collection named "Tour" for the database with the "tourSchema" structure and saving the schema in the Tour
const Tour = mongoose.model('Tour', tourSchema);

//export mdule
module.exports = Tour;
